#include <string.h>

int	ft_checkgrid(char *str)
{
	int i;

	i = 0;
	while (str[i] && i < 20)
	{
		if ((i + 1) % 5 == 0 && str[i] != '\n' && i != 0)
			return (0);
		if ((i + 1) % 5 != 0 && str[i] != '.' && str[i] != '#')
			return (0);
		i++;
	}
	return (1);
}
